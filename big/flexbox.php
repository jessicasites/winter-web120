<p>Flexbox is a designer tool that makes it easier to design flexible responsive layouts. 

Flex layouts are flexible and adjusts to get the best readability. Flex layout is writing 

mode aware, meaning it is not confined in assuming that a line of text will be written left 

to right and it adjusts accordingly. It is versatile and able to switch axis from row to 

column. It is an efficient way to align and space elements in your document. No matter 

how many items you put into a flexbox, it will adjust to fit all of them, even if the browser 

needs to scroll horizontally. Flex gives you many, easy to use options for adjusting and 

specifying your layout and is a great tool to have in your toolbox. 

