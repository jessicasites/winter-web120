<p>When it comes to choosing the best shopping cart for your website, you’ll need to 

consider who your target audience is, the cost, how much time and maintenance you 

want to put in, and what specific features you are looking for. 

There are many shopping cart options out there and can be built using ASP, Java, JS, 

HTML etc. Each has its advantages and disadvantages, with HTML being the most 

versatile and compatible with different browsers and devices. If you don’t have the time 

to build a cart from scratch however, there are many ready-to-go software options out 

there. These include open source, which is free, but would have limited tech support 

and you’d end up doing a lot of the required upgrades and maintenance yourself. There 

are also lots of licenced software library options to choose from. These would not be 

free, but would include more tech support, and you’d most likely still be responsible for 

upgrades and maintenance yourself. 

The best option seems to be using a hosted platform, which provides a web based 

interface for you to manage your site, there is no need to download or maintain anything 

as this is handled by the company. You do pay for the service, but in the long run having 

a more streamlined process for your customers, will equal happier customers and help 

you retain those customers making you more money in the long run. 

