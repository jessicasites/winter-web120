<p>When deciding whether to choose a responsive web design or a mobile app, you should first consider your audience and how much time and money you are willing/able to invest. 
Responsive web design reaches a bigger audience due to its availability on both a desktop and mobile device and it’s cheaper to create. It is easier for the user to quickly do a search and find the site on either device than it would be to download or possibly purchase an app. However, it can be slower and less efficient to load onto mobile devices. 
On the other hand, an app is way more efficient and customized to the mobile experience. If cost is a concern though, you may want to go with responsive, since to develop an app you’ll need more time and a team of specialized developers to create this for you. You may also need to develop two apps for multiple operating systems, which would equate to even more time and money spent.  
In making your decision, it really comes down to what your site will be used for, who your target audience is, and how much time and money you can afford. 

